/**
 * Class representing keyboard input state.
 * Tracks the state (pressed/not pressed) of various keyboard keys.
 */
class Keyboard {
    /** @type {boolean} State of the left arrow key */
    LEFT = false;
    /** @type {boolean} State of the right arrow key */
    RIGHT = false;
    /** @type {boolean} State of the up arrow key */
    UP = false;
    /** @type {boolean} State of the down arrow key */
    DOWN = false;
    /** @type {boolean} State of the space bar */
    SPACE = false;
    /** @type {boolean} State of the D key */
    D = false;
}